// NOLINTNEXTLINE(modernize-use-trailing-return-type)
TEST(SimplexTest, BasicAssertions) {
  // 10 >= 3x + 2y + z
  // 15 >= 2x + 5y + 3z
  IntMatrix A{"[10 3 2 1; 15 2 5 3]"_mat};
  IntMatrix B{DenseDims{0, 4}};
  BumpAlloc<> alloc;
  Optional<Simplex *> optS0{Simplex::positiveVariables(alloc, A)};
  EXPECT_TRUE(optS0.hasValue());
  Optional<Simplex *> optS1{Simplex::positiveVariables(alloc, A, B)};
  EXPECT_TRUE(optS1.hasValue());
  assert(optS0.hasValue());
  assert(optS1.hasValue());
  for (size_t i = 0; i < 2; ++i) {
    Simplex &S{i ? *optS1 : *optS0};
    auto C{S.getCost()};
    // minimize -2x - 3y - 4z
    C[0] = 0;
    C[1] = 0;
    C[2] = 0;
    C[3] = -2;
    C[4] = -3;
    C[5] = -4;
    llvm::errs() << "S.tableau =" << S.getTableau() << "\n";
    EXPECT_EQ(S.run(), 20);
  }
}

// NOLINTNEXTLINE(modernize-use-trailing-return-type)
TEST(LexMinSmallTest, BasicAssertions) {
  BumpAlloc alloc;
  // -10 == -3x - 2y - z  + s0
  // -15 == -2x - 5y - 3z + s1
  IntMatrix tableau{"[-10 0 1 -1 -2 -3; -15 1 0 -3 -5 -2]"_mat};
  // IntMatrix A{"[-10 -3 -2 -1; -15 -2 -5 -3]"_mat};
  Simplex *simp{Simplex::create(alloc, 2, 5)};
  simp->getConstraints() << tableau;
  Vector<Rational> sol(5);
  EXPECT_FALSE(simp->initiateFeasible());
  llvm::errs() << "S.tableau =" << simp->getTableau() << "\n";
  //       x  y  z s0  s1 a0 a1
  // [  0  0  0  0  0  0  1  1
  //   10  3  2  1 -1  0  1  0
  //   15  2  5  3  0 -1  0  1 ]
  //       x  y  z s0  s1 a0 a1
  // [  0 -5 -7 -4  1  1  0  0
  //   10  3  2  1 -1  0  1  0
  //   15  2  5  3  0 -1  0  1 ]
  // first entering variable: x
  // first leaving variable: a0
  //       x    y   z s0   s1  a0  a1
  // [-25   0 -11  -7  -2   3   5   0    f: 3
  //   10   3   2   1  -1   0   1   0
  //   25   0  11   7   2  -3  -2   3 ]
  // second entering variable: y
  // second leaving variable: a1
  //       x    y   z  s0  s1  a0  a1
  // [  0   0   0   0   0   0   3   3    f: 3
  //   60  33   0  -3 -15   6  15  -6
  //   25   0  11   7   2  -3  -2   3 ]
  // drop augment vars, divide rows by gcd
  //       x    y   z  s0  s1
  // [  0   0   0   0   0   0    f: 3
  //   20  11   0  -1  -5   2
  //   25   0  11   7   2  -3 ]
  //
  //
  //        x  y  z  s0 s1
  // [ 20  11  0 -1 -5 -30
  //   25  0  11  7  2  1 ]
  // x = 20 // 11
  // y = 25 // 11
  // -3*20/11 - 2*25/11 = (-60 - 50)/11 = -110/11 = -10
  // -2*20/11 - 5*25/11 = (-40 - 125)/11 = -165/11 = -15
  // our initial tableau is feasible
  //
  // [  0  11  0  0  0   0
  //   20  11  0 -1 -5 -30
  //   25  0  11  7  2  1 ]
  //
  // [-20   0  0  1  5  30
  //   20  11  0 -1 -5 -30
  //   25  0  11  7  2  1 ]

  simp->rLexMin(sol);
  llvm::errs() << "S.tableau =" << simp->getTableau() << "\n";
  //  0  1  2  3  4
  //  0  0 10  0 15
  // 70  0  0  0 25
  //
  // What should happen
  //
  // -10 == -3x - 2y - z  + s0
  // -15 == -2x - 5y - 3z + s1
  llvm::errs() << "sol = " << sol << "\n";
  EXPECT_EQ(Rational::create(5, 1), 5);
  EXPECT_EQ(sol[last - 0], 0);
  EXPECT_EQ(sol[last - 1], 0);
  EXPECT_EQ(sol[last - 2], 10);
  EXPECT_EQ(sol[last - 3], 0);
  EXPECT_EQ(sol[last - 4], 15);
}

auto simplexFromTableau(BumpAlloc<> &alloc, IntMatrix &tableau)
  -> NotNull<Simplex> {
  unsigned numCon = unsigned(tableau.numRow()) - 1;
  unsigned numVar = unsigned(tableau.numCol()) - 1;
  Simplex *simp{Simplex::create(alloc, numCon, numVar)};
  simp->getTableau() << tableau;
  return simp;
}

